<p>Los Angeles Lakers es un equipo de baloncesto profesional de la NBA con sede en Los Ángeles, California. Juegan sus partidos de local en el 
Staples Center, compartiendo pabellón con Los Angeles Clippers, Los Angeles Sparks de la WNBA y Los Angeles Kings de la NHL entre otros equipos.
Han sido campeones de la NBA en 16 ocasiones, solo detrás del equipo más ganador de la historia de la NBA, los Boston Celtics.1​ Poseen el 
récord de más partidos consecutivos ganados (33) en la historia del deporte profesional estadounidense</p>
<h4>Orígenes y cambios de ciudad</h4>
<p>Los orígenes de Lakers estan en Detroit, allí un tal C. King Boring crea una franquicia de baloncesto llamada los Detroit Gems, este equipo 
solo disputaría una temporada en la NBL. Pero a los Gems no les fueron bien las cosas, y al finalizar esa primera temporada, Boring vendería los 
Gems a Ben Berger y Morris Chalfen, por 15.000 dolares, los cuales llevarían la franquicia a Minneapolis, era el año 1947.
<p>Berger y Chalfen  llevan su franquicia recién comprada al Minneapolis Auditorium y deciden cambiarle el nombre por el de “Lakers”, en 
referencia a los miles de  lagos que rodean Minneapolis. En el año 1948 los Lakers saltan a la BBA. En 1949, la BAA y la NBL se fusionan para 
convertirse en la NBA.</p>
<p>El 25 de abril de 1960, Lakers llegan a California, y pasan a llamarse, Los Angeles Lakers, permaneciendo allí hasta la actualidad</p>

<h4>Títulos</h4>
<p>Los Angeles Lakers han sido campeones de la NBA en los años  1949, 1950, 1952, 1953, 1954, 1972, 1980, 1982, 1985, 1987, 1988, 2000, 2001, 
2002, 2009 y 2010, son el segundo equipo que más campeonatos ha conseguido solo por detrás de los Boston Celtics.</p>

<h4>Propietarios</h4>
<p>Gerald Hatten Jerry Buss fue un empresario y químico estadounidense. Desde el año 1979 hasta su fallecimiento el 18 de febrero de 2013 fue el propietario mayoritario</p>
<p>Desde su fallecimiento el equipo paso a cargo de su familia, ejercicio de presidente Magic Johnson, famoso exjugador de los Lakers.</p>

